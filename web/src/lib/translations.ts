function getNestedValue(obj: Record<string, unknown>, path: string): string {
  const parts = path.split('.')
  let current: unknown = obj
  for (const part of parts) {
    if (current == null || typeof current !== 'object') return path
    current = (current as Record<string, unknown>)[part]
  }
  return typeof current === 'string' ? current : path
}

export function createT(language: Language) {
  const dict = translations[language] as unknown as Record<string, unknown>
  const fallback = translations['en'] as unknown as Record<string, unknown>
  return function t(key: string, vars?: Record<string, string | number>): string {
    let value = getNestedValue(dict, key)
    if (value === key && language !== 'en') {
      value = getNestedValue(fallback, key)
    }
    if (vars) {
      return value.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''))
    }
    return value
  }
}
